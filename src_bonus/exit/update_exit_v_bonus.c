#include "so_long_bonus.h"

void	update_exit_v(t_game *g)
{
	if (-12 <= g->exit.v.y + g->exit.a.y && g->exit.v.y + g->exit.a.y <= 12)
		g->exit.v.y += g->exit.a.y;
}
